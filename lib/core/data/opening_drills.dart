  static ChessDrill getDrillForSubtopic(String topicId, String subtopic) {
    final topic = topics.firstWhere(
      (item) => item.id == topicId,
      orElse: () => topics.first,
    );
    final topicLabel = topic.label;
    final index = topic.subtopics.indexOf(subtopic);
    return UniversalTestDrillProvider.buildDrill(
      phase: DrillPhase.opening,
      topicId: topic.id,
      topicLabel: topicLabel,
      subtopic: index >= 0 ? topic.subtopics[index] : subtopic,
      difficulty: (index >= 0 ? index : 0) + 1,
    );
  }
}

class OpeningDrillsData {
  OpeningDrillsData._();

  static List<OpeningTopic> get allTopics =>
      UniversalTestDrillProvider.buildTopics(
        sourceTopics: OpeningDrillData.topics,
        phase: DrillPhase.opening,
      );

  static ChessDrill getDrillForSubtopic(String topicId, String subtopic) {
    final topic = allTopics.firstWhere(
      (item) => item.id == topicId,
      orElse: () => allTopics.first,
    );
    final drills = topic.getDrillsForSubtopic(subtopic);
    return drills.isNotEmpty
        ? drills.first
        : OpeningDrillData.getDrillForSubtopic(topic.id, subtopic);
  }
}
